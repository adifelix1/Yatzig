    private void quantityReq_update()
    {
          try{
                 String sql = "select item_id as 'Item ID',user_name as 'User Name',old_quantity as 'Old Quantity',new_quantity as 'New Quantity',comments as 'Comments',status as 'Status' from quantity_change";
                 pst=conn.prepareStatement(sql);
                 rs=pst.executeQuery(sql);
                 QuantityRequestTable.setModel(DbUtils.resultSetToTableModel(rs));
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null, e);
        }
    }
    
    private void checkIfTableEmpty(){
           int checkEmpty=0;
           try{
            String sql = "select * from quantity_change";
            pst = conn.prepareStatement(sql);
            rs = pst.executeQuery(sql);
            if(rs.next())
            checkEmpty=1;
        }
        catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
        if(checkEmpty==0)
           {   
          iconChange();
           }
       }
    
